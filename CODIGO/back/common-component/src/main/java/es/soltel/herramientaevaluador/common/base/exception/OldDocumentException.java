package es.soltel.herramientaevaluador.common.base.exception;

public class OldDocumentException extends Exception {

    private static final long serialVersionUID = -8263054821191770123L;

    public OldDocumentException() {
        super();
    }

    public OldDocumentException(String message, Throwable cause) {
        super(message, cause);
    }

    public OldDocumentException(String message) {
        super(message);
    }

    public OldDocumentException(Throwable cause) {
        super(cause);
    }

}
